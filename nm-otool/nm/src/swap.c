#include "nm.h"

uint16_t			swap_u16(uint16_t nb)
{
	return ((nb >> 0x08) | (nb << 0x08));
}

uint32_t			swap_u32(uint32_t nb)
{
	return (((nb >> 0x18) & 0xff) |
			((nb >> 0x08) & 0xff00) |
			((nb << 0x08) & 0xff0000) |
			((nb << 0x18) & 0xff000000));
}

uint64_t			swap_u64(uint64_t nb)
{
	return (((nb >> 0x38) & 0xff) |
			((nb >> 0x28) & 0xff00) |
			((nb >> 0x18) & 0xff0000) |
			((nb >> 0x08) & 0xff000000) |
			((nb << 0x08) & 0xff00000000) |
			((nb << 0x18) & 0xff0000000000) |
			((nb << 0x28) & 0xff000000000000) |
			((nb << 0x38) & 0xff00000000000000));
}
